<span style="white-space:normal;">
<b>Is this like Dungeons & Dragons?</b><br /><br />
</span>
<div style="white-space:normal;margin-left:10px;">
It can be. There are dice rollers and stuff. You don't have to play it like that if you don't want to.</div>
<br /><br />
<span style="white-space:normal;">
<b>Okay, how do I get started?</b><br /><br />
</span>
<div style="white-space:normal;margin-left:10px;">
You log in by clicking the button in the menu bubble. (If you don't see a menu, click "menu" at the bottom of the page.) Then you can create a character. Or create a new quest if you want to be the GM.</div>
